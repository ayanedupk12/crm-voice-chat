class AdditionalSection {
  String? _titleName;
  int? _sectionIcon;
  String? _count;

  String? get titleName => _titleName;
  set titleName(String? value) => _titleName = value;

  int? get sectionIcon => _sectionIcon;
  set sectionIcon(int? value) => _sectionIcon = value;

  String? get count => _count;
  set count(String? value) => _count = value;

 
}
